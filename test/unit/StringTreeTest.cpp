static std::vector<std::string> buildSubtree(const std::string& prefix,
                                             char seed,
                                             int size) {
  std::vector<std::string> names;
  for (int c = (unsigned char)seed; size > 0; size--, c++) {
    names.push_back(prefix + c);
  }

  return names;
}

