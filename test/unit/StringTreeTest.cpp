static std::string toHexRepresentation(const std::string& str) {
  std::ostringstream hex_str;
  for (char c : str) {
    hex_str << std::hex << std::setw(2) << std::setfill('0')
            << static_cast<int>(c);
  }
  std::string hex = hex_str.str();
  return hex;
}

